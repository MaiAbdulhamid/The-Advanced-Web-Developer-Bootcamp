import React, { Component } from 'react';
import './App.css';
const NUM_BOXS = 32;

const Box = ({color}) => {
  const style = {
    backgroundColor: color,
    width: '180px',
    height: '180px',
    display: 'inline-block'
  }

  return <div style={style} />
}
class App extends Component {
  
  constructor(props) {
    super(props);
    const boxs = Array(NUM_BOXS).fill().map(this.getRandColor, this)
    this.state = {boxs: boxs}
    setInterval(() => {
      const boxs = this.state.boxs.slice()
      const randIndx = Math.floor(Math.random() * boxs.length)
      boxs[randIndx] = this.getRandColor();
      this.setState({boxs})
    }, 400)
  }
  getRandColor(){
    let randIndex = Math.floor(Math.random() * this.props.allColors.length)
    return this.props.allColors[randIndex]
  }
  render() {
    const boxs = this.state.boxs.map((color, i) => {
      return <Box key={i} color={color} />
    })
    return (
      <div className="App">
        {boxs}
      </div>
    );
  }
}

